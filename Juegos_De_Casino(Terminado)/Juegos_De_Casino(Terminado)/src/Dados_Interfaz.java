    private void Btn_TiralosdadosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_TiralosdadosActionPerformed
        /// Generaremos el tiro de los dados y usaremos numeros de manera aleatoria
        int dado1 = (int)(Math.random()*6)+1;
        int dado2 = (int)(Math.random()*6)+1;
        
        //Generacion de iconos 
        Icon uno = new javax.swing.ImageIcon(getClass().getResource("/Dados/Uno.png"));
        Icon dos = new javax.swing.ImageIcon(getClass().getResource("/Dados/Dos.png"));
        Icon tres = new javax.swing.ImageIcon(getClass().getResource("/Dados/Tres.png"));
        Icon cuatro = new javax.swing.ImageIcon(getClass().getResource("/Dados/Cuatro.png"));
        Icon cinco = new javax.swing.ImageIcon(getClass().getResource("/Dados/Cinco.png"));
        Icon seis = new javax.swing.ImageIcon(getClass().getResource("/Dados/Seis.png"));
             
        switch(dado1){
            case 1: LblDado1.setIcon(uno); break;
            case 2: LblDado1.setIcon(dos); break;
            case 3: LblDado1.setIcon(tres); break;
            case 4: LblDado1.setIcon(cuatro); break;
            case 5: LblDado1.setIcon(cinco); break;
            case 6: LblDado1.setIcon(seis); break;
        }
        
        switch(dado2){
            case 1: LblDado2.setIcon(uno); break;
            case 2: LblDado2.setIcon(dos); break;
            case 3: LblDado2.setIcon(tres); break;
            case 4: LblDado2.setIcon(cuatro); break;
            case 5: LblDado2.setIcon(cinco); break;
            case 6: LblDado2.setIcon(seis); break;
        }
        
        int resultado = dado1 + dado2;
        jTxt1.setText("Te salio!!!: " + resultado);
        
    }//GEN-LAST:event_Btn_TiralosdadosActionPerformed

